package com.javaoktato.phonebook.domain;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElementWrapper;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "phonebook")
@XmlAccessorType(XmlAccessType.FIELD)
public class PhoneBook {
	@XmlElement(name = "person")
	@XmlElementWrapper
	private List<Person> entries;

	public PhoneBook() {
		entries = new ArrayList<>();
	}

	public PhoneBook(List<Person> entries) {
		this.entries = entries;
	}

	public List<Person> getEntries() {
		return entries;
	}

	public void setEntries(List<Person> entries) {
		this.entries = entries;
	}

	public void addEntry(Person p) {
		entries.add(p);
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((entries == null) ? 0 : entries.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PhoneBook other = (PhoneBook) obj;
		if (entries == null) {
			if (other.entries != null)
				return false;
		} else if (!entries.equals(other.entries))
			return false;
		return true;
	}
}
